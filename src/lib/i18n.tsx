type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  ts: (slug: string) => ServiceI18n | undefined;
};

const LangContext = createContext<Ctx | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("pt");

  useEffect(() => {
    const stored = (typeof window !== "undefined" && localStorage.getItem("lang")) as Lang | null;
    if (stored && ["pt", "en", "es"].includes(stored)) setLangState(stored);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem("lang", l); } catch { /* ignore */ }
    if (typeof document !== "undefined") document.documentElement.lang = l;
  };

  const t = (key: string) => dicts[lang][key] ?? dicts.pt[key] ?? key;
  const ts = (slug: string) => (lang === "pt" ? undefined : serviceI18n[slug]?.[lang]);

  return <LangContext.Provider value={{ lang, setLang, t, ts }}>{children}</LangContext.Provider>;
}

export function useT() {
  const ctx = useContext(LangContext);
  if (!ctx) return { lang: "pt" as Lang, setLang: () => {}, t: (k: string) => dicts.pt[k] ?? k, ts: () => undefined };
  return ctx;
}
